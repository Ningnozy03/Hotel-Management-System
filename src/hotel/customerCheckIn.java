    public void roomDetails()
    {
        ckbRoomNo.removeAllItems();
        txtPrice.setText("");
        bedType = (String)ckbBed.getSelectedItem();
        roomType = (String)ckbRoomType.getSelectedItem();
        try
        {
            ResultSet rs = Select.getData("select *from room where bedType = '" + bedType + "' and roomType = '" + roomType + "' and status = 'Not Booked'");
            while(rs.next())
            {
                ckbRoomNo.addItem(rs.getString(1));
            }
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }

