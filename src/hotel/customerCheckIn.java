    private void btnAlloteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlloteActionPerformed
        int id = 1;
String name = txtName.getText();
String mobileNum = txtMobile.getText();
String address = txtAddress.getText();
String nationality = txtNation.getText();
String gender = (String) ckbGender.getSelectedItem();
String email = txtEmail.getText();
String ssNum = txtSsNum.getText();
String checkIn = txtDate.getText();
String bedType = (String) ckbBed.getSelectedItem();
String roomType = (String) ckbRoomType.getSelectedItem();
String roomNo = (String) ckbRoomNo.getSelectedItem();
String price = txtPrice.getText(); 
String Query = "select max(id) from customer";

try {
    ResultSet rs = Select.getData(Query);
    while (rs.next())
        id = rs.getInt(1);
    id = id + 1;

    if (!price.equals("")) 
    {
        Query = "update room set status='Booked' where roomNo='"+roomNo+"'";
        InsertUpdateDelete.setData(Query, "");
        Query = "insert into customer(id,name,mobileNum,address,nationality,gender,email,ssNum,checkIn,roomNo,bedType,roomType,pricePerDay) values("+id+",'"+name+"','"+mobileNum+"','"+address+"','"+nationality+"','"+gender+"','"+email+"','"+ssNum+"','"+checkIn+"','"+roomNo+"','"+bedType+"','"+roomType+"','"+price+"')";
        
        InsertUpdateDelete.setData(Query, "Customer Check-in Successful");
        setVisible(false);
        new customerCheckIn().setVisible(true);
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, e);
}

    }//GEN-LAST:event_btnAlloteActionPerformed

