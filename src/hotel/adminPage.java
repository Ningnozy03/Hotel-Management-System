    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String username = model.getValueAt(index,0).toString();
        String status = model.getValueAt(index,2).toString();
        if(status.equals("true"))
            status="false";
        else 
            status = "true";
        try
        {
            int a = JOptionPane.showConfirmDialog(null, "Do you want to change status "+username+"","Select",JOptionPane.YES_NO_OPTION);
            if (a == 0)
            {
                InsertUpdateDelete.setData("UPDATE users SET status = '" + status + "' WHERE username = '" + username + "'", "Status changed successfully");
                setVisible(false);
                new adminPage().setVisible(true);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jTable1MouseClicked

