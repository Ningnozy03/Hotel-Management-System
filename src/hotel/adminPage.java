    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String usernameOrEmail = txtSearch.getText(); 
        ResultSet rs = Select.getData("SELECT * FROM users WHERE username LIKE '%" + usernameOrEmail + "%' OR email LIKE '%" + usernameOrEmail + "%'");
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

    try     
    {
        while (rs.next()) {
            model.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(4)});
        }
        rs.close();
    } 
    catch (Exception e) 
    {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_btnSearchActionPerformed

